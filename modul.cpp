#include "modul.h"

using namespace std;

double mnoz(double i, double j)
{
    auto Wynik = i*j;
    return Wynik;
}

double dziel(double i, double j)
{
    auto Wynik = i/j;
    return Wynik;
}

double dodaj(double i, double j)
{
    auto Wynik = i+j;
    return Wynik;
}

double odejmij(double i, double j)
{
    auto Wynik = i-j;
    return Wynik;
}
